#Sets are unordered collections of unique elements. They do not allow duplicates.
# Create a set
my_set = {1, 2, 3,2, 4, 5}

# Add an element
my_set.add(6)

# Remove an element
my_set.remove(3)

# Check for membership
print(4 in my_set)  # Output: True

# Iterate over the set
for item in my_set:
    print(item)
